#define WIN32_LEAN_AND_MEAN
#define _WINSOCK_DEPRECATED_NO_WARNINGS

#include "EasyTcpServer.hpp"

int main() {
	EasyTcpServer server;
	server.Bind(nullptr, 4567);
	server.Listen(5);
	while (server.isRun()) {
		server.OnRun();
		//printf("空闲时间处理其它业务。。。。\n");
	}
	server.Close();
	printf("任务结束，程序退出 \n");
	getchar();
	return 0;
}
